"""Метрики Prometheus.

Модель Prometheus — pull, а не push: приложение никуда ничего не отправляет,
а просто копит счётчики в памяти процесса и отдаёт их снимок по GET /metrics
в текстовом формате. Prometheus сам, по расписанию (scrape), приходит и
забирает эти данные.

Два типа метрик используются здесь:
- Counter   — только растёт (общее число запросов с начала работы процесса).
  Из накопленного значения PromQL считает "запросов в секунду" через rate().
- Histogram — распределение значений по бакетам (задержки ответа). Из него
  Prometheus считает перцентили (p50/p95/p99) через histogram_quantile() —
  в отличие от среднего, перцентили не "прячут" редкие медленные запросы.

Метка `path` — ШАБЛОН роута (например "/links/{short_code}"), а НЕ сырой
путь запроса ("/links/abc123"). Это принципиально: если использовать сырой
путь, каждый уникальный короткий код (или просто мусорный запрос сканера
вроде "/wp-admin") породил бы свою метку. Количество временных рядов
(кардинальность) росло бы неограниченно — вплоть до того, что хранилище
метрик перестаёт справляться с нагрузкой. Это одна из самых частых ошибок
новичков с Prometheus.
"""

from prometheus_client import Counter, Histogram
from starlette.requests import Request

REQUESTS_TOTAL = Counter(
    "http_requests_total",
    "Общее количество HTTP-запросов",
    ["method", "path", "status"],
)

REQUEST_DURATION_SECONDS = Histogram(
    "http_request_duration_seconds",
    "Время обработки HTTP-запроса, в секундах",
    ["method", "path", "status"],
)


def route_label(request: Request) -> str:
    """Метка `path` для метрик — шаблон роута, а не сырой URL.

    request.scope["route"] заполняется Starlette-роутером к моменту, когда
    call_next() возвращает управление (маршрутизация уже случилась). Если
    роут не найден (запрос не подошёл ни под один маршрут — настоящий 404
    на уровне роутинга, а не бизнес-логики) — используем фиксированную
    метку "unmatched", а не реальный путь: иначе кардинальность метрик
    росла бы от каждого случайного/сканирующего запроса.
    """
    route = request.scope.get("route")
    if route is not None:
        return route.path
    return "unmatched"
