#!/bin/sh
# Точка входа контейнера app.
# 1) применяем миграции БД, 2) запускаем веб-сервер.

# set -e — упасть сразу, если любая команда вернёт ошибку.
# Важно: если миграции не применились, поднимать приложение бессмысленно —
# лучше упасть громко, чем работать на неверной схеме БД.
set -e

echo "==> Применяю миграции Alembic..."
alembic upgrade head

echo "==> Запускаю Uvicorn..."
# exec заменяет процесс sh на процесс uvicorn, отдавая ему PID 1.
# Зачем PID 1: именно первому процессу Docker шлёт сигналы (SIGTERM при
# `docker stop`). Без exec сигнал пришёл бы в sh, а uvicorn его не увидел бы →
# не было бы graceful shutdown, контейнер убивали бы через таймаут (SIGKILL).
exec uvicorn app.main:app --host 0.0.0.0 --port 8000
